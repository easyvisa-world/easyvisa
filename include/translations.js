// Функция для получения перевода
function getTranslation(key, lang = null) {
  const currentLang = lang || getCurrentLanguage();
  const keys = key.split('.');
  let translation = window.TRANSLATIONS[currentLang];

  for (const k of keys) {
    if (translation && (translation[k] !== undefined)) {
      translation = translation[k];
    } else {
      // Fallback to Russian if translation not found
      translation = window.TRANSLATIONS['RU'];
      for (const fallbackKey of keys) {
        if (translation && (translation[fallbackKey] !== undefined)) {
          translation = translation[fallbackKey];
        } else {
          return key; // Return key if no translation found
        }
      }
      break;
    }
  }

  return translation;
}

// Функция для получения текущего языка
function getCurrentLanguage() {
  return localStorage.getItem('ev_lang') || localStorage.getItem('selectedLang') || 'RU';
}

// Функция для применения переводов к странице
function applyTranslations() {
  const currentLang = getCurrentLanguage();

  // Обновляем все элементы с data-translate
  document.querySelectorAll('[data-translate]').forEach(element => {
    const key = element.getAttribute('data-translate');
    const translation = getTranslation(key, currentLang);

    if (element.hasAttribute('data-translate-html')) {
      element.innerHTML = translation;
    } else {
      // Обработка списков и переносов строк
      if (translation.includes('\n')) {
        if (element.tagName === 'UL' || element.tagName === 'OL') {
          // Для списков
          const items = translation.split('\n').filter(item => item.trim());
          element.innerHTML = '';
          items.forEach(item => {
            const li = document.createElement('li');
            li.innerHTML = item.trim();
            element.appendChild(li);
          });
        } else {
          // Для обычных элементов с переносами
          element.innerHTML = translation.replace(/\n/g, '<br>');
        }
      } else {
        element.textContent = translation;
      }
    }
  });
}

// Инициализация переводов при загрузке страницы
function initTranslations() {
  applyTranslations();

  // Слушаем изменения языка
  document.addEventListener('languageChanged', function() {
    applyTranslations();
  });
}

// Автоматическая инициализация
if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initTranslations);
} else {
  initTranslations();
}
