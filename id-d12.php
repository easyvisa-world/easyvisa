        el.style.display = '';
      });

      // Обновляем lang атрибут html элемента
      document.documentElement.lang = lang === 'en' ? 'en' : 'ru';

      // Сохраняем выбор
      localStorage.setItem('ev_lang', lang.toUpperCase());
